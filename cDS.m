% (10.1),(1.4) Mathcad DS10_4

function b = cDS(Lmn)

switch numel(Lmn)
case 1
   b = zeros(3,1);
   b(1) = (64+17*Lmn)/45;   b(2) = (28-Lmn)/90;   b(3) = (8+19*Lmn)/15;
case 2
   b = zeros(4,1);
   La = Lmn(1); mu = Lmn(2);
   b(1) = (81+167*La+1088*mu)/420;    b(2) = (81-La+38*mu)/52.5;
   b(3) = (1107+5*La-64*mu)/3780;     b(4) = (459+293*La+332*mu)/236.25;
case 3
   b = zeros(5,1); 
   La = Lmn(1);  mu = Lmn(2);  nu = Lmn(3);
   b(1) = (167936 + 23189*La + 70528*mu +  94041*nu)/56700;    
   b(2) = ( -7424 -   701*La + 10988*mu + 115911*nu)/28350;
   b(3) = ( 94208 +   167*La -   896*mu +  58563*nu)/56700; 
   b(4) = ( 31648 -    23*La +   104*mu -   2187*nu)/113400;
   b(5) = (-14528 + 13903*La +  8696*mu +  50787*nu)/11340;
end